	<h1>Versenkung</h1>
	<h2>26. Juli 1916</h2>
	<p>Da die Gefahr zunahm, dass der Gegner die Trasse der Mittellandbahn erreichen und diese unterbrechen k&ouml;nte, mu&szlig;ten die Deutschen damit beginnen, Kigoma zu r&auml;umen und sich nach Tabora zur&uuml;ckzuziehen, um nicht abgeschnitten zu werden. Um zu verhindern, dass die &ldquo;Goetzen&rdquo; in feindliche H&auml;nde fiel, gab Korvettenkapit&auml;n Zimmer den Befehl, sie zu versenken. Ausf&uuml;hren sollten diesen Befehl ausgerechnet die drei Papenburger Schiffbauer, die all die Strapazen und Entbehrungen auf sich genommen hatten, dass Schiff vor mehr als zwei Jahren an den Tanganjika-See zu bringen und anschlie&szlig;end dort zusammenzubauen. Doch wie sollten sich die M&auml;nner verhalten? Nat&uuml;rlich hatten sie einen Befehl erhalten, der auszuf&uuml;hren war, gerade in Zeiten des Krieges. Andererseits jedoch hatten sie in dieses schwierige Projekt zuviel M&uuml;he und Entbehrungen gesteckt, um es einfach blindlings zerst&ouml;en zu k&ouml;nen. So gelangten sie zu dem Plan, am 26. Juli 1916 zun&auml;chst die Gesch&uuml;tze von dem Schiff zu entfernen, da diese f&uuml;r die weiteren Kampfhandlungen noch verwendet werden konnten. Anschlie&szlig;end entfernten sie einige der wichtigsten Teile der Dampfmaschine, die sie an Land versteckten. Die restlichen Maschinenteile fetteten sie sorgf&auml;ltig ein, um sie vor Korrosion zu sch&uuml;tzen. Die Ingenieure hatten die Hoffnung, dass Deutschland den Krieg gewinnen w&uuml;rde und man nach Kigoma zur&uuml;ckkehren und das Schiff bergen k&ouml;nte. Zuletzt &uuml;berluden sie es mit Sand (andere Quellen nennen Zement) und versenkten es in kontrollierter Weise nachts in der seichten M&uuml;ndung des Malagarasiflusses, da dort das Wasser weniger klar war. Ein kleineres Boot, die &ldquo;Adjutant&rdquo;, lag auf der Helling und wurde gesprengt, w&auml;hrend mit der &ldquo;Wami&rdquo; ein weiteres ebenfalls versenkt wurde.
	</p>

	<h1>Bergung durch Belgien</h1>
	<h2>1916</h2>
	<p>Nachdem Deutschland seine Kolonien verloren hatte, gelangte das Gebiet am Tanganjika-See zun&auml;chst fast vollst&auml;ndig unter die Kontrolle der Belgier. Aufgrund von Informationen der Einheimischen gelang es ihnen rasch, das Schiff zu finden. Es gelang ihnen bereits 1916, die &ldquo;Goetzen&rdquo; zu heben und sie zur&uuml;ck nach Kigoma zu schleppen.
	</p>

