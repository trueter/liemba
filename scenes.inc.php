	<h1>Vormontage</h1>
	<h2>Feb. 1913</h2>
	<p>Das Schiff wird auf der Meyer-Werft in Papen-burg zum ersten Mal provisorisch zusammengesetzt, um sicherzuge-hen, dass alles passt.)
	Die Teile des ersten Schiffes wurden bei der Meyer-Werft in Papenburg gefertigt und erfolgreich vormontiert. Dies geschah unter der Anleitung und &uuml;berwachung des Germanischen Lloyd f&uuml;r die Klasse &ldquo;100 A 4 Tanganjikasee&rdquo;. Bei dieser ersten Vormontage wurden die Schiffsteile jedoch nicht zusammengenietet, sondern lediglich lose zusammengeschraubt. Auf der Helling wurde die Konstruktion des provisorisch zusammengef&uuml;gten Schiffes gepr&uuml;ft, wobei sogar die Dampfkessel befeuert und die Dampfmaschine einem Testlauf unterzogen wurden. F&uuml;r diese Vorarbeiten ben&ouml;igte man zehn Monate, von Februar bis November 1913.
	</p>

	<h1>Technische Daten des Schiffes</h1>
	<h2>14. Apr. 1913</h2>
	<p>Laut einer Bescheinigung des Germanischen Lloyd vom 14. April 1913 sollte das Schiff 67 Meter lang und 10 Meter breit sein, sowie &uuml;ber eine Verdr&auml;ngung von 1.150 Tonnen verf&uuml;gen. Sein Rumpf sollte &uuml;ber der Wasserlinie 3,4 Meter hoch sein und &uuml;ber einen Tiefgang von 3,2 Metern verf&uuml;gen, dies jeweils bei einer Beladung mit 480 Tonnen Ladung, 60 Tonnen Kohle und 10 Tonnen Wasser. Seine zwei Dreifach-Expansions-Dampfmaschinen sollten eine Leistung von zusammen 500 PS erbringen und die &uuml;ber einen Durchmesser von zwei Metern verf&uuml;genden Schrauben sollten dem Schiff bei 155 U/min und in ruhigem Wasser zu einer Geschwindigkeit von 9.5 Knoten verhelfen. Bei Testfahrten im Juni 1915 wurden allerdings nur 7 Knoten erreicht; die erzielte H&ouml;hstgeschwindigkeit lag bei 9 Knoten.
	</p>
