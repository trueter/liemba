	<h1>Erneuerung Schiff und Leuchtfeuer</h1>
	<h2>1924</h2>
	<p>Am 17. September 1924 erkl&auml;rte sich das britische Au&szlig;enministerium in einem Schreiben an den Gouverneur grunds&auml;tzlich damit einverstanden, das Schiff f&uuml;r den Dienst auf dem Tanganjika-See wiederherzurichten, und bat um eine komplette Aufstellung der veranschlagten Kosten.
