		Im Februar 2012 fand ein Parlamentarischer Abend zum Thema statt. Eine Instandsetzung des Schiffes wurde erneut unter s&auml;mtlichen Aspekten beleuchtet, erneut mit positivem Ergebnis.
		Im April 2012 fanden in Dar es Salaam bilaterale Regierungsgespr&auml;che zwischen Deutschland und Tansania statt. Auf Veranlassung des anwesenden BMZ-Vertreters wurde im Protokoll vermerkt, dass sich Tansania alleine um die Instandsetzung bzw. Modernisierung der Schiffahrt auf Njassa-, Tanganjika- und Viktoriasee k&uuml;mmern w&uuml;rde.  
