		Doch auch dieses Angebot war nicht zu halten, da sich w&auml;hrend der anschlie&szlig;enden dreij&auml;hrigen Instandsetzungsphase diverse weitere Instandsetzungsarbeiten als erforderlich herausstellen sollten, und schlie&szlig;lich lagen die Kosten bei insgesamt 29.296 &pound;. Rechnet man hierzu noch die durch die Belgier und die Briten durchgef&uuml;hrten Bergungskosten in H&ouml;e von 20.400 &pound; hinzu, so kam man auf Gesamtkosten von 49.696 &pound;. Hinzu kamen laut einem Schreiben des Generalmanagers vom 6. August 1926 noch geringere Betr&auml;ge f&uuml;r die Erneuerung der Werft (15.000 &pound;), der Hellinganlage (10.000 &pound;) und den Bau eines Krans (3.000 &pound;). Au&szlig;erdem wurden noch Seezeichen und Leuchtfeuer an den &ouml;stlichen und s&uuml;dlichen Ufern des Sees installiert.
