	<h1>Dieselmotoren</h1>
	<h2>Nov 1976</h2>
	<p>Zu jener Zeit gelangte mit Patrick Dougherty ein pensionierter irischer Schiffsingenieur an den Tanganjika-See, und wie erz&auml;hlt wird, verliebte er sich vom ersten Moment in dieses eindrucksvolle und faszinierende St&uuml;ck alter Ingenieurskunst. M&ouml;licherweise war seine Pers&ouml;lichkeit vergleichbar mit derjenigen der drei deutschen Werftmitarbeiter, welche das Schiff mehr als ein halbes Jahrhundert zuvor erbaut hatten. Ger&uuml;chten zufolge verlie&szlig; Dougherty sogar Frau und Kind, um seine ganze Aufmerksamkeit dem Wiederaufbau der &ldquo;Liemba&rdquo; zu widmen. Er hatte den eisernen Willen, das Schiff wiederherzurichten und wieder in Fahrt zu sehen. Nachdem er sich diesbez&uuml;glich der Unterst&uuml;tzung des tansanischen Pr&auml;sidenten Julius Nyerere versichern konnte und durch die Weltbank und aus T&ouml;fen diverser Entwicklungshilfsprogramme finanziert wurde, begann er mit seinem Werk. Vor allem wurden die Passagierkapazit&auml;ten von bis dahin 430 auf nunmehr 600 Personen vergr&ouml;szlig;ert sowie die alte Dampfmaschine durch zwei britische Caterpillar-Dieselmotoren ersetzt. Nach zwei Jahren waren die Arbeiten im November 1976 beendet. Die &ldquo;Liemba&rdquo; von nun an als MS (Motorschiff) bzw. MV (motor-vessel) bezeichnet, konnte wieder in Dienst gestellt werden. Patrick Dougherty wurde im Jahre 1978 Leiter der Schiffseinsatzstelle Kigoma. 
	</p>

