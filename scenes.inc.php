	<h1>Zwei offizielle deutsche Delegationen</h1>
	<h2>2009-2012</h2>
	<p>So haben Run "Liemba"-Mitglieder im September 2009 vier leitenden Angestellten des MSCL einen Informationsbesuch in Deutschland erm&ouml;glicht. Ziele waren der Bodensee, Berlin und Papenburg an der Ems.
		Im Jahr 2010 kam es zur Reise eines Mitarbeiters der Firma "Hamburg Port Consult", einer Firma f&uuml;r Hafenlogistik (!) nach Kigoma. Diese war durch die Kreditanstalt f&uuml;r Wiederaufbau beauftragt worden, eine technische Machbarkeitsstudie zur Instandsetzung des Schiffes zu entwickeln, und diese wiederum durch das Bundesministerium f&uuml;r wirtschaftliche Zusammenarbeit und Entwicklung. Die extrem negativen Ergebnisse des Gutachtens stellten sich nachtr&auml;glich als nicht belastbar heraus, da der Gutachter die "Liemba" nachweislich nicht betreten hatte und sie in seinem Gutachten zudem mit einem anderen Schiff verwechselt hatte! Dessen ungeachtet diente dieses Papier dem BMZ lange Zeit weiter als Argumentationshilfe.
		In der ersten Jahresh&auml;lfte 2011 reisten erneut Fachleute nach Kigoma und inspizierten die "Liemba". Diesmal mit positivem Ergebnis.
