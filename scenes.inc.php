	<h2>10 Juni 1916</h2>
	<p>Am 10. Juni 1916 flogen vier belgische Wasserflugzeuge einen ersten Bombenangriff gegen das DS &ldquo;Goetzen&rdquo;, das zu diesem Zeitpunkt im Hafen von Kigoma lag. Nach dem Angriff entsandten die Belgier Aufkl&auml;rungsflugzeuge, um sich einen genaueren &uuml;berblick &uuml;ber die erzielten Besch&auml;digungen zu verschaffen. Nach ihrer R&uuml;ckkehr berichteten die belgischen Piloten, dass sie eine Rauchwolke aus dem Achterdeck des Schiffes hatten aufsteigen sehen. Ein paar Tage sp&auml;ter f&uuml;hrten die Aufkl&auml;rer einen neuen Erkundungsflug nach Kigoma durch. Nachdem sie erkannten, dass das Schiff dort auf der Helling lag und von den Deutschen repariert wurde, bombardierten sie es erneut.
