	<h1>Erste Pl&auml;ne</h1>
	<h2>Juni 1910</h2>
	<p> Nachdem die Kolonialverwaltung sich bei der Eisenbahnverwaltung in Tabora &uuml;ber 
		die Ausma&szlig;e britischer Dampfer auf dem Viktoria-See erkundigte und um 
		n&auml;here Informationen &uuml;ber sinnvolle Gr&ouml;&szlig;en entsprechender 
		Einheiten auf dem Tanganjika-See bat, wird der Bau eines gr&ouml;&szlig;eren 
		Dampfschiffes auf dem Tanganjika-See erstmals angedacht.
	</p>

	<h1>Bestellung des Schiffes</h1>
	<h2>1912</h2>
	<p> Als sich 1912 ein Ende der Bauarbeiten an der Mittellandbahn abzeichnet, 
		bestellt die OAEG bei der Werft Joseph L. Meyer in Papenburg an der Ems bis 
		sp&auml;testens Januar 1913 ein Passagier- und Frachtschiff mit Doppelschraubenantrieb 
		f&uuml;r den Dienst auf dem Tanganjika-See.
	</p>
</div>

<!-- ocean scene -->

<div class="historyText not-yet-implemented">
	<!-- #2 -->
	<h1>Vormontage</h1>
	<h2>Feb. 1913</h2>
	<p> Das Schiff wird auf der Meyer-Werft in Papenburg zum ersten Mal provisorisch zusammengesetzt, 
		um sicherzugehen, dass alles passt. Die Teile des ersten Schiffes werden bei der Meyer-Werft 
		in Papenburg gefertigt und erfolgreich vormontiert. Dies geschieht unter der Anleitung und 
		&Uuml;berwachung der Germanischen Lloyd. 
		Bei der Vormontage werden die Schiffsteile jedoch nicht zusammengenietet, sondern 
		lediglich lose zusammengeschraubt. Auf der Helling wird die Konstruktion des provisorisch 
		zusammengef&uuml;gten Schiffes gepr&uuml;ft, wobei sogar die Dampfkessel befeuert und die 
		Dampfmaschine einem Testlauf unterzogen werden. Die Vorarbeiten sind nach 10 Monaten im 
		November 1913 abgeschlossen.
	</p>

	<h1>Technische Daten des Schiffes</h1>
	<h2>Apr. 1913</h2>
	<p> Laut einer Bescheinigung der Germanischen Lloyd vom 14. April 1913 soll das Schiff 67 Meter lang 
		und 10 Meter breit sein, sowie &uuml;ber eine Verdr&auml;ngung von 1.150 Tonnen verf&uuml;gen. 
		Sein Rumpf soll dabei 3,4 Meter &uuml;ber der Wasserlinie hoch sein und &uuml;ber einen Tiefgang von 
		3,2 Metern verf&uuml;gen, dies jeweils bei einer Beladung mit 480 Tonnen Fracht, 60 Tonnen Kohle 
		und 10 Tonnen Wasser. Seine zwei Dreifach-Expansions-Dampfmaschinen sollen eine Leistung von 
		zusammen 500 PS erbringen und die &uuml;ber einen Durchmesser von zwei Metern verf&uuml;genden 
		Schrauben sollen dem Schiff bei 155 U/min und in ruhigem Wasser zu einer Geschwindigkeit von 9.5 
		Knoten verhelfen. Bei Testfahrten im Juni 1915 werden allerdings nur 7 Knoten erreicht; die erzielte 
		H&ouml;hstgeschwindigkeit lag bei 9 Knoten.
	</p>
</div>

