<div class="historyText not-yet-implemented">
	<!-- #3 -->
	<h1>Start des Transports</h1>
	<h2>Nov. 1913</h2>
	<p> Nach dem Testlauf wird das Schiff unter der Aufsicht eines Chefingenieurs der Werft wieder in seine 
		Einzelteile zerlegt, in 5.000 wasserdichte Kisten verpackt und per Bahn von Papenburg zum Hamburger 
		Hafen transportiert.
	</p>

	<h1>Ablauf des Transports</h1>
	<h2>1913/1914</h2>
	<p> Die Einzelteile des Schiffes werden mit vier &Uuml;berseedampfern transportiert. 
		Die &ldquo;Admiral&rdquo; und die &ldquo;Feldmarschall&rdquo; laufen am 19. Dezember 1913, 
		die &ldquo;Windhoek&rdquo; am 12. Januar 1914 und die &ldquo;Adolf Woermann&rdquo; am 27. Januar 1914 
		via Mittelmeer und Suezkanal nach Dar es Salaam aus, der Hauptstadt der Kolonie Deutsch-Ostafrika. 
		Die Ladung der beiden ersten Schiffe enthält neben den Teilen f&uuml;r den Aufbau einer kompletten 
		Werft zur Endmontage am Zielort auch s&auml;mtliches Material f&uuml;r den Schiffsrumpf. 
		Die &ldquo;Windhoek&rdquo; transportiert die Masten, die Dampfkessel und die Dampfmaschine sowie weitere 
		Zubeh&ouml;rteile, w&auml;hrend die &ldquo;Adolf Woermann&rdquo; die Decksh&auml;user, die Ausstattung 
		der Innenr&auml;ume und Kabinen sowie Installations- und Ersatzteile liefert. 
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #4 -->
	<h1>Ankunft</h1>
	<h2>Jan 1914</h2>
	<p> In Dar es Salaam angekommen, werden die Kisten der &ldquo;Goetzen&rdquo; auf Waggons der 
		Mittellandbahn verladen und nach Kigoma transportiert. Die Mittellandbahn ist im November 1913 jedoch
		noch nicht vollst&auml;ndig fertiggestellt, weshalb ihre Gleise 34 Kilometer vor Kigoma enden.
		Da die Zeit dr&auml;ngt, mu&szlig;ten die Teile durch hunderte afrikanische Tr&auml;ger nach Kigoma  
		geschafft werden. Die mit den Teilen geplante Werft wird aus einer elektrisch betriebenen Helling, 
		einem elektrischen Kraftwerk sowie einer Werkstatt bestehen, die &uuml;ber s&auml;mtliche Kr&auml;ne, 
		Werkzeuge und Maschinen verf&uuml;gt, die zum Zusammenbau des Schiffes und seiner weiteren 
		Instandhaltung ben&ouml;igt werden.
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #5 -->
	<h1>Berichte der Werftmitarbeiter </h1>
	<h2>Feb. 1914</h2>
	<p> Nach ihrer Ankunft in Kigoma engagieren die drei Werftmitarbeiter zun&auml;chst afrikanische und 
		indische Arbeiter f&uuml;r die Endmontage des Schiffes. Die Montage, w&auml;hrend der der verheerende 
		Erste Weltkrieg ausbrichz, dauert mehr als ein Jahr. <br>
		Aus dieser Zeit sind noch Berichte von Meister R&uuml;ter an die Meyer-Werft erhalten, die ein bezeichnendes 
		Licht auf die Situation werfen, welche sich den drei Werftmitarbeitern im tiefsten Herzen Afrikas bot: <br>

		&ldquo;Au&szlig;er einer fehlenden Platte des Vordecks haben wir jetzt alles in Kigoma. Es fehlen nur zwei Kisten 
		mit Deckschrauben, die irgendwo im Hafen von Dar es Salaam verloren gegangen sein m&uuml;ssen &hellip;&rdquo; <br>
		
		&ldquo;Die Taue, die auf einem offenen Eisenbahnwagen transportiert worden waren, sind durch ein Feuer 
		w&auml;hrend der Bahnfahrt vollkommen unbrauchbar geworden&hellip;&rdquo; <br>

		&ldquo;Au&szlig;er einem unf&auml;higen Schreiner und einem kompetenten Elektriker habe ich hier keine 
		europ&auml;ischen Helfer finden k&ouml;nen. Ich besch&auml;ftige jetzt 20 flei&szlig;ige Inder und 150 
		Schwarze. Wenn das Nieten beginnt, werden vermutlich 100 zus&auml;tzliche Schwarze gebraucht werden&hellip;&rdquo; <br>

		&ldquo;Ich mu&szlig;te die elektrische Hauptanlage alleine herstellen, und seit ein paar Tagen arbeitet 
		unser Kran ohne Probleme. Aber das Feuer w&auml;hrend der Bahnfahrt hat eine Propellerwelle verbogen&hellip;&rdquo; <br>

		&ldquo;Wir sind alle noch gesund und haben bis jetzt kein Fieber. Wir haben zu wenig Khakianz&uuml;ge und 
		Schuhe. Die Arbeit geht gut voran, auf da&szlig; das Schiff gut dicht wird&hellip;&rdquo; <br>

		&ldquo;Wir arbeiten nun schon seit drei Wochen an dem Schiff und hoffen auf den Stapellauf im August&hellip;&rdquo; <br>
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #6 -->
	<h1>Erster Termin f&uuml;r Stapellauf </h1>
	<h2>Jan. 1915</h2>
	<p> Ein Telegramm an die Eisenbahnverwaltung in Tabora vom Januar 1915 k&uuml;ndigt den voraussichtlichen Termin 
		des Stapellaufes f&uuml;r den 25. Januar an, und ein weiteres Schreiben erw&auml;hnt als Termin 
		f&uuml;r die Jungfernfahrt des Schiffes und seine &Uuml;bergabe an ein Marinekommando der Schutztruppe 
		eine Woche Ende Mai. Zu beiden Ereignissen wurden die Vertreter der Eisenbahnverwaltung eingeladen. Aufgrund 
		eines Schadens am Propellerschaft waren diese Termine jedoch nicht zu halten.
	</p>

	<h1>Stapellauf</h1>
	<h2>Feb. 1915</h2>
	<p>Am 5. Februar 1915 gleitet das Schiff zum ersten Mal von der Helling ins Wasser. Leider gestaltet sich dies 
		etwas schwieriger als geplant, da nach Kriegsausbruch einige wichtige Teile der Helling Kigoma nicht mehr 
		erreicht haben. Doch durch das Improvisationsgeschick der drei Papenburger 
		Werftmitarbeiter ist es dennoch gelungen. 
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #7 -->
	<h1>&Uuml;bergabe an Schutztruppe und Waffenausr&uuml;stung</h1>
	<h2>Mai 1915</h2>
	<p>Mit dem Einsetzen der ersten Kampfhandlungen am Tanganjika-See im Mai 1915, wird befohlen, das kaum 
		fertiggestellte Schiff an eine Marineeinheit der deutschen Schutztruppe zu &uuml;bergeben, um auf diese 
		Weise die lange Westgrenze Deutsch-Ostafrikas gegen die gegnerischen belgischen und britischen Nachbarn 
		zu sichern bzw. zu verteidigen. Aus diesen Gr&uuml;nden werden ein 10.5-cm-Gesch&uuml;tz, zwei 
		8.8-cm-Gesch&uuml;tze sowie drei 3.7-cm-Revolver-Kanonen auf dem Deck des Schiffes montiert.
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #8 -->
	<h1>Fertige Aussttatung und die erste Fahrt</h1>
	<h2>Juni 1915</h2>
	<p>Nach dem Stapellauf wird das Schiff fertig ausger&uuml;stet und der Bau endlich abgeschlossen.
		Am Morgen des 1. Juni 1915 absolviert das Schiff mit dem Ziel Bismarckburg seine erste Fahrt. 
		Bei rauhem Wellengang und einem starken Wind in der Buch von Kungwe befinden sich etwa 700 
		Angeh&ouml;igen der Schutztruppe an Bord. Das Schiff kommt zun&auml;chst &uuml;berhaupt nicht voran.
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #9 -->
	<h1>Erste offizielle Probefahrt</h1>
	<h2>Juni 1915</h2>
	<p>Am 8. und 9. Juni 1915 werden im ruhigen Wasser zwischen Kigoma und Lugufu auf einer Strecke von insgesamt 
		nur 33 Seemeilen die offiziellen Probefahrten absolviert. Dabei wird am ersten Tag mit frischem, 
		unentrindetem Holz eine Durchschnittsgeschwindigkeit von lediglich 7 Knoten erzielt. Als am zweiten Tag 
		trockenes und entrindetes Holz verwendet wird, liegt die Durchschnittsgeschwindigkeit bei 8,25 Knoten.
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #10 -->
	<h1>Erster Kampfeinsatz</h1>
	<h2>Juni 1915</h2>
	<p>Unter dem Kommano von Korvettenkapit&auml;n Zimmer bricht das das DS &ldquo;Goetzen&rdquo; zu seiner 
		Jungfernfahrt nach Bismarckburg, dem heutigen Kasanga, auf. Wenig sp&auml;ter findet am 19. Juni 1915 
		der erste Kampfeinsatz des Schiffes statt, als es vor Mpulungu den kleineren britischen 
		Dampfer &ldquo;Cecil Rhodes&rdquo; versenkt. Bis Mitte 1916 wird das Schiff f&uuml;r diverse 
		weitere Patrouillen- und Versorgungsfahrten verwendet und unterst&uuml;tzt damit den Oberbefehlshaber 
		der Schutztruppen von Deutsch-Ostafrika, den sp&auml;teren General von Lettow-Vorbeck.
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #11 -->
	<h1>Aufgabe von Bismarckburg</h1>
	<h2>Juni 1916</h2>
	<p>Die Belgier die Festung Bismarckburg von der See, bis die Deutschen diese am 6. Juni 1916 aufgeben 
		und sich nach Tabora zur&uuml;ckziehen. In diesem Zeitraum errichten die Belgier in Albertville zudem eine 
		Basis f&uuml;r Wasserflugzeuge, mit denen sie Bombenangriffe auf deutsche Ziele in und um Kigoma fliegen.
	</p>

	<h1>Bombenangriff auf "Goetzen"</h1>
	<h2>Juni 1916</h2>
	<p>Am 10. Juni 1916 fliegen vier belgische Wasserflugzeuge einen ersten Bombenangriff gegen das 
		DS &ldquo;Goetzen&rdquo;, das zu diesem Zeitpunkt im Hafen von Kigoma liegt. Nach dem Angriff 
		entsenden die Belgier Aufkl&auml;rungsflugzeuge, um sich einen genaueren &Uuml;berblick &uuml;ber 
		den erzielten Schaden zu verschaffen. Nach ihrer R&uuml;ckkehr berichten die belgischen 
		Piloten, dass sie eine Rauchwolke aus dem Achterdeck des Schiffes haben aufsteigen sehen. Ein paar 
		Tage sp&auml;ter f&uuml;hren die Aufkl&auml;rer einen neuen Erkundungsflug nach Kigoma durch. Als 
		sie erkennen, dass das Schiff dort auf der Helling liegt und von den Deutschen repariert wird, bombardieren 
		sie es erneut.
	</p>
</div>
<div class="historyText not-yet-implemented">
	<!-- #12 -->
	<h1>Versenkung</h1>
	<h2>Juli 1916</h2>
	<p>Als die Gefahr zunimmt abgeschnitten zu werden, da der Gegner die Trasse der Mittellandbahn erreichen 
		und unterbrechen k&ouml;nte, m&uuml;ssen die Deutschen damit beginnen, Kigoma zu r&auml;umen und sich 
		nach Tabora zur&uuml;ckzuziehen. Um zu verhindern, dass die &ldquo;Goetzen&rdquo; 
		in feindliche H&auml;nde fällt, gibt Korvettenkapit&auml;n Zimmer am 26. Juli den Befehl, sie zu versenken. 
		Ausf&uuml;hren sollen diesen Befehl ausgerechnet die drei Papenburger Schiffbauer, die all die 
		Strapazen und Entbehrungen auf sich genommen haben, dass Schiff vor mehr als zwei Jahren an den 
		Tanganjika-See zu bringen und anschlie&szlig;end dort zusammenzubauen. Doch wie sollen sich die 
		M&auml;nner verhalten? Einerseits müssen Sie dem Befehl Folge leisten aber andererseits hatten sie in das Schiff
		soviel Mühe und Entbehrung gesteckt. So gelangten sie zu dem Plan, die Gesch&uuml;tze vom Schiff zu entfernen, 
		da diese f&uuml;r weitere Kampfhandlungen noch verwendet werden können. 
		Anschlie&szlig;end entfernen sie wichtige Teile der Dampfmaschine, die sie an Land verstecken. In der Hoffnung, 
		dass Deutschland den Krieg gewinnen w&uuml;rde, fetten sie die restlichen Maschinenteile sorgfältig ein, um sie
		vor Korrosion zu schützen. Zuletzt &uuml;berladen sie es mit Sand oder Zement
		und versenken es in kontrollierter Weise nachts in der seichten M&uuml;ndung des Malagarasiflusses.
		Auf der Helling wurde stattdessen ein kleineres Boot, die &ldquo;Adjutant&rdquo;, gesprengt.
	</p>

	<h1>Bergung durch Belgien</h1>
	<h2>1916</h2>
	<p>Nachdem Deutschland seine Kolonien verloren hat, gelangt das Gebiet am Tanganjika-See zun&auml;chst 
		fast vollst&auml;ndig unter die Kontrolle der Belgier. Aufgrund von Informationen der Einheimischen 
		gelingt es ihnen rasch, die &ldquo;Goetzen&rdquo; zu finden und es noch im gleichen Jahr zu heben und 
		sie zur&uuml;ck nach Kigoma zu schleppen.
	</p>


</div>
<div class="historyText not-yet-implemented">
	<!-- #13 -->
	<h1>Erneuter Untergang</h1>
	<h2>1924</h2>
	<p>Ungl&uuml;cklicherweise sinkt die &ldquo;Goetzen&rdquo; vermutlich im Jahre 1920 dort an ihrem Liegeplatz in 
		seichtem Wasser erneut, nach unterschiedlichen Aussagen entweder aufgrund eines Hebefehlers oder w&auml;hrend 
		eines heftigen Sturmes.
	</p>

	<h1>Bergung durch Briten</h1>
	<h2>M&auml;rz 1924</h2>
	<p>Nach einem gescheiterten Bergungsversuch der Belgier, starten die Briten eine erneute Bergung.
		Diese stellt sich jedoch als langwierigeres und komplizierteres Vorhaben heraus, und die Gesamtkosten betragen 
		(einschlie&szlig;lich des gescheiterten Bergungsversuches der Belgier) das Dreifache der urspr&uuml;nglich
		veranschlagten Kosten. Die Arbeit dauert mehr als zwei Jahre und erfolgt unter der Leitung von 
		Lieutenant-Commander T. Kerr von der Royal Navy. Nach nahezu acht Jahren unter Wasser taucht die 
		&ldquo;Goetzen&rdquo; am 16. M&auml;rz 1924 wieder auf und pr&auml;sentiert sich nach einer ersten 
		Untersuchung zur allgemeinen &uuml;berraschung in einem unerwartet guten Zustand.
	</p>

</div>
<div class="historyText not-yet-implemented">
	<!-- #14 -->
	<h1>Schiffserneuerung und Leuchtfeuer</h1>
	<h2>1924</h2>
	<p>Am 17. September 1924 erkl&auml;rt sich das britische Au&szlig;enministerium in einem Schreiben an den 
		Gouverneur grunds&auml;tzlich damit einverstanden, das Schiff f&uuml;r den Dienst auf dem Tanganjika-See 
		wiederherzurichten, und bittet um eine komplette Aufstellung der veranschlagten Kosten.
		Solch ein detaillierter Kostenvoranschlag ist bereits im Mai durch den Chefmechaniker K. C. Strahan 
		angefertigt worden. Um das Schiff in einen seetauglichen Zustand zu bringen, veranschlagt Strahan f&uuml;r 
		den Rumpf, die Einrichtung, die Decks usw. 12.422 &pound; und f&uuml;r den technischen Teil wie Dampfmaschine, 
		Kessel sowie weitere Aggregate 7.481 &pound;.<br>
		Doch auch dieses Angebot war nicht zu halten, da sich w&auml;hrend der anschlie&szlig;enden dreij&auml;hrigen 
		Instandsetzungsphase diverse weitere Instandsetzungsarbeiten als erforderlich herausstellen sollen. 
